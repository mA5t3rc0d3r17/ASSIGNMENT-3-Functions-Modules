# Import math module for mathematical operations
import math

# Get user input
num = int(input("Enter a number: "))

# Calculate square root using math.sqrt()
sq_root = math.sqrt(num)

# Calculate natural logarithm using math.log()
natlog = math.log(num)

# Calculate sine value using math.sin() (input in radians)
sine = math.sin(num)

# Print all calculated values
print("Square root: ", sq_root)
print("Logarithm: ", natlog)
print("Sine: ", sine)